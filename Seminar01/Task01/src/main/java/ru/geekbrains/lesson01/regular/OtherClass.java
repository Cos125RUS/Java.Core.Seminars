package ru.geekbrains.lesson01.regular;

/**
 * Mathematics actions class
 */
public class OtherClass {
    /**
     * sum
     * @param a num1
     * @param b num2
     * @return res
     */
    public static int sum(int a, int b) {
        return a + b;
    }

    /**
     * div
     * @param a num1
     * @param b num2
     * @return res
     */
    public static int div(int a, int b) {
        return a / b;
    }

    /**
     * mul
     * @param a num1
     * @param b num2
     * @return res
     */
    public static int mul(int a, int b) {
        return a * b;
    }

    /**
     * sub
     * @param a num1
     * @param b num2
     * @return res
     */
    public static int sub(int a, int b) {
        return a - b;
    }
}
